package org.lastbamboo.server.resource;

/**
 * Exception for an unexpected value.
 */
public class UnexpectedValueException extends Exception
    {

    public UnexpectedValueException(final String msg)
        {
        super(msg);
        }

    /**
     * Generated serial id.
     */
    private static final long serialVersionUID = 5309535265785719740L;

    }
